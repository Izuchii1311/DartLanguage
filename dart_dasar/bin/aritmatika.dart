void main() {
  int a = 10;
  int b = 5;
  int c = 2;

  var totalPenjumlahan = a + b + c;
  print('Total Penjumlahan: $totalPenjumlahan');
  var totalPengurangan = a - b - c;
  print('Total Pengurangan: $totalPengurangan');
  var totalPerkalian = a * b * c;
  print('Total Perkalian: $totalPerkalian');
  var totalPembagian = a / b / c;
  print('Total Pembagian: $totalPembagian');
  var angkaNegatif = a + -b;
  print('Total Pertambahan dengan angka Negatif: $angkaNegatif');
  var pembagianHasilInt = a ~/ c;
  print('Total Pembagian hasil Int: $pembagianHasilInt');
  var sisaBagi = a % c;
  print("Sisa Bagi: $sisaBagi");
}
